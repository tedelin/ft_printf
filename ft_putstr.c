#include "ft_printf.h"
#include <unistd.h>

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str && str[i])
		ft_putchar(str[i++]);
	return (i);
}
